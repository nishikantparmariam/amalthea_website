        <script>

            //Initialising Firebase database and storing it in a variable 'db'
            var db = firebase.database();


            //send() function stores our message into database whenever button is clicked
            function send(){

                //Grabbing the elements to extract text from them
                var name = document.getElementById('name');
                var msg = document.getElementById('msg');


                //Extracting text and storing them in 'nameText' and 'msgText'
                var nameText = name.value;
                var msgText = msg.value;       



                //Checking if both the texts are non-empty
                if(nameText!=""&&msgText!=""){

                    //Generating a unique key
                    var key = db.ref().push().key;


                    //Storing data into Firebase database at location '/messages/{key}'
                    db.ref('/messages/'+key).set({
                            "name":nameText,
                            "msg":msgText
                    });


                    //Setting the input field's value to empty string
                    name.value = "";
                    msg.value = "";

                } else {


                    //Alerting the user if all details are not filled
                    alert("Please fill all the details");
                }
            }


            
            //fetch_data() function fetchs all messages and display them inside 'msg_container' div
            function fetch_data(){


                //Below code is like a 'loop' which gets called for each message (old/new) in the database
                db.ref('/messages/').on('child_added', function(data){




                    // Variable 'data' contains the actual data stored in the database
                    var name = data.val().name;
                    var msg = data.val().msg;   

                    //Dynamically creating a 'div' tag 
                    var div = document.createElement('div');


                    //Putting some HTML inside it
                    div.innerHTML = "<hr>@"+name+ ": "+msg+"";   


                    //Appending the newly form 'div' to 'msg_container' div
                    document.getElementById("msg_container").append(div);


                    //Scrolling to the bottom
                    document.getElementById("msg_container").scrollTop =  document.getElementById("msg_container").scrollHeight;  


                });
            }            
        </script>
