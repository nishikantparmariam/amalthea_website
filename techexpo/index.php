        function validateEmail(email) {
            var re = /^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/;
            return re.test(String(email).toLowerCase());
        }

        function showAlert(msg){

                $(".alertShowInner").html(msg);
                $(".alertShow").fadeIn();
                setTimeout(function(){
                $(".alertShow").fadeOut();
                }, 2000);
            
        }
        function submitSubscribeInfo(){

    
            var SubscribeName = $("#SubscribeName").val().trim();
            var SubscribeEmail =  $("#SubscribeEmail").val().trim();
            if((SubscribeName==""||SubscribeEmail=="")||(SubscribeName==""&&SubscribeEmail=="")) {
                $(".alertSubscribeTop").html("Please fill all the details");
                showAlert("Please fill all the details");
            } else {
                if(validateEmail(SubscribeEmail)){
                    var xmlhttp = new XMLHttpRequest();
                    xmlhttp.onreadystatechange = function() {
                        if (this.readyState == 4 && this.status == 200) {
                            var response = this.responseText;
                            if(response=="done"){
                                $(".alertSubscribeTop").html("Please check your mail");                                
                                showAlert("Please check your mail");

                            }
                            else {
                                $(".alertSubscribeTop").html("Please try again");
                                showAlert("Please try again");
                            }
                        }
                    };
                    xmlhttp.open("GET", "../sendSubscribeMail.php?SubscribeName="+SubscribeName+"&SubscribeEmail="+SubscribeEmail, true);
                    xmlhttp.send();
                } else {
                    $(".alertSubscribeTop").html("Please fill a valid email ID");
                    showAlert("Please fill a valid email ID");
                }
            }
        } 
